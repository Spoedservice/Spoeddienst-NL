// Generate all keyword combinations
export function generateAllKeywords(serviceSlug) {
  const data = KEYWORDS_DATABASE[serviceSlug];
  const meta = SERVICE_META[serviceSlug];
  const keywords = [];
  
  // General keywords
  data.general.forEach(kw => {
    keywords.push({ keyword: kw, type: "general", priority: "high" });
  });
  
  // Problem keywords
  data.problems.forEach(kw => {
    keywords.push({ keyword: kw, type: "problem", priority: "high" });
  });
  
  // City combinations
  data.cities.forEach(city => {
    // Service + city
    keywords.push({ 
      keyword: `${meta.slug} ${city}`, 
      type: "city", 
      priority: "high",
      city 
    });
    keywords.push({ 
      keyword: `spoed ${meta.slug} ${city}`, 
      type: "city", 
      priority: "high",
      city 
    });
    keywords.push({ 
      keyword: `${meta.slug} ${city} 24 uur`, 
      type: "city", 
      priority: "high",
      city 
    });
    keywords.push({ 
      keyword: `${meta.slug} ${city} spoed`, 
      type: "city", 
      priority: "high",
      city 
    });
    
    // Problem + city for high priority problems
    const topProblems = data.problems.slice(0, 5);
    topProblems.forEach(problem => {
      keywords.push({ 
        keyword: `${problem} ${city}`, 
        type: "problem_city", 
        priority: "medium",
        city 
      });
    });
  });
  
  return keywords;
}

